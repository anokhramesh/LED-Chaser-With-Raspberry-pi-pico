from machine import Pin
from time import sleep

led1 = Pin(10, Pin.OUT)
led2 = Pin(11, Pin.OUT)
led3 = Pin(12, Pin.OUT)
led4 = Pin(13, Pin.OUT)
led5 = Pin(14, Pin.OUT)
led6 = Pin(15, Pin.OUT)
def forward (a,b,c,d,e,f):
    led1.value(a)
    led2.value(b)
    led3.value(c)
    led4.value(d)
    led5.value(e)
    led6.value(f)
    sleep(0.25)
def reverse (a,b,c,d,e,f):
    led1.value(f)
    led2.value(e)
    led3.value(d)
    led4.value(c)
    led5.value(b)
    led6.value(a)
while True:
    forward(0,0,0,0,0,0)
    forward(0,0,0,0,0,1)
    forward(0,0,0,0,1,1)
    forward(0,0,0,1,1,1)
    forward(0,0,1,1,1,1)
    forward(0,1,1,1,1,1)
    forward(1,1,1,1,1,1)
    forward(1,1,1,1,1,0)
    forward(1,1,1,1,0,0)
    forward(1,1,1,0,0,0)
    forward(1,1,0,0,0,0)
    forward(1,0,0,0,0,0)
    #sleep(1)
    reverse(1,0,0,0,0,0)
    reverse(1,1,0,0,0,0)
    reverse(1,1,1,0,0,0)
    reverse(1,1,1,1,0,0)
    reverse(1,1,1,1,1,0)
    reverse(0,0,0,0,0,1)
    reverse(0,0,0,0,1,1)
    reverse(0,0,0,1,1,1)
    reverse(0,0,1,1,1,1)
    reverse(0,1,1,1,1,1)
